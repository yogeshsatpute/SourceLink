namespace CalculationLib
{
    public class Calculate
    {
        public int Add(int value1, int value2)
        {
            return value1 + value2;
        }

        public int Sub(int value1, int value2)
        {
            return value1 - value2;
        }

        public int Mul(int value1, int value2)
        {
            return value1 * value2;
        }
    }
}
